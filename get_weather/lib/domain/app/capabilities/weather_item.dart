class WeatherItem {
  final double? windSpeed;
  final double? temp;
  final double? pressure;
  final double? humidity;
  final double? tempFeelsLike;
  final DateTime? date;

  WeatherItem({
    this.windSpeed,
    this.temp,
    this.pressure,
    this.humidity,
    this.tempFeelsLike,
    this.date,
  });
}
